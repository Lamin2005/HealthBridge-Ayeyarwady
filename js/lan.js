// Load the saved language from localStorage or default to English
let currentLanguage = localStorage.getItem("selectedLanguage") || "en";

// Function to toggle language
function toggleLanguage() {
  // Switch between English and Myanmar
  currentLanguage = currentLanguage === "en" ? "my" : "en";

  // Save the selected language to localStorage
  localStorage.setItem("selectedLanguage", currentLanguage);

  // Update the UI with the new language
  const elements = document.querySelectorAll("[data-key]");
  elements.forEach((element) => {
      const key = element.getAttribute("data-key");

      // Change text content based on the selected language
      element.innerText = translations[currentLanguage][key];
  });

  // Update the button text and color
  const button = document.querySelector(".language-toggle");
  button.innerText = translations[currentLanguage].button;
  button.style.backgroundColor = translations[currentLanguage].buttonColor;
}

// Initial setup to load the saved language on page load
document.addEventListener("DOMContentLoaded", () => {
  const elements = document.querySelectorAll("[data-key]");
  elements.forEach((element) => {
      const key = element.getAttribute("data-key");
      element.innerText = translations[currentLanguage][key];
  });

  const button = document.querySelector(".language-toggle");
  button.innerText = translations[currentLanguage].button;
  button.style.backgroundColor = translations[currentLanguage].buttonColor;
});
